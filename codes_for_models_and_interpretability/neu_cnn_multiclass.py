"""End-to-end multiclass CNN pipeline for microstructure image classification,
with detailed misclassification analysis and optional Score-CAM explanations.

This cell:
1. Sets seeds, mounts Google Drive, and loads a folder-structured multiclass
   microstructure dataset (one subdirectory per class), resizing and
   normalizing images, encoding labels, and saving a stratified train/
   validation/test split for reuse.
2. Applies strong data augmentation (rotations, flips, height shifts) and
   class weighting to address limited data and imbalance, then defines a
   multiclass CNN with a dedicated penultimate convolutional layer for
   visualization, global max pooling, and L2-regularized dense layers.
3. Phase 1: Performs stratified K-fold cross-validation, logging per-fold
   accuracy, macro/weighted F1, and per-class precision/recall/F1, and
   saving training curves and confusion matrices to disk.
4. Phase 2: Trains a final CNN on the full augmented training set with
   checkpointing, learning-rate scheduling, and CSV logging, evaluates on
   the held-out test set, and stores global performance metrics and
   misclassification counts.
5. Visualizes misclassified test samples with top-2 predicted classes and
   confidences, summarizes confusion patterns and per-class error rates,
   and optionally generates multiclass Score-CAM heatmaps for a diverse
   subset of correct, misclassified, and low-confidence test images to
   inspect class-specific attention patterns.
"""

