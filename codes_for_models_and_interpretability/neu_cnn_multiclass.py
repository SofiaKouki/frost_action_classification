print(f"Total misclassified: {len(misclassified_idx)} out of {len(y_test)} ({len(misclassified_idx)/len(y_test)*100:.1f}%)")

if len(misclassified_idx) > 0:
    n_samples = min(12, len(misclassified_idx))

    fig = plt.figure(figsize=(16, 12))

    for i, idx in enumerate(misclassified_idx[:n_samples]):
        plt.subplot(3, 4, i + 1)
        img = X_test[idx]
        plt.imshow(np.clip(img, 0, 1))
        plt.axis('off')

        true_class = class_names[y_test[idx]]
        pred_class = class_names[y_test_pred[idx]]
        confidence = y_test_pred_probs[idx, y_test_pred[idx]]

        # Get top 2 predictions for context
        top2_idx = np.argsort(y_test_pred_probs[idx])[-2:][::-1]
        second_pred = class_names[top2_idx[1]]
        second_conf = y_test_pred_probs[idx, top2_idx[1]]

        plt.title(
            f"True: {true_class}\n"
            f"Pred: {pred_class} ({confidence:.2f})\n"
            f"2nd: {second_pred} ({second_conf:.2f})",
            fontsize=9,
            color='red'
        )

    plt.suptitle(f"Misclassified Test Images (n={len(misclassified_idx)})",
                 fontsize=16, fontweight='bold')
    plt.tight_layout(rect=(0, 0, 1, 0.96))
    plt.savefig(os.path.join(save_dir, 'cnn_misclassified_images.png'),
                dpi=300, bbox_inches='tight')
    plt.show()

    # Confusion pattern analysis
    print("\n" + "="*40)
    print("CONFUSION PATTERNS")
    print("="*40)

    confusion_pairs = {}
    for idx in misclassified_idx:
        true_label = class_names[y_test[idx]]
        pred_label = class_names[y_test_pred[idx]]
        pair = f"{true_label} → {pred_label}"
        confusion_pairs[pair] = confusion_pairs.get(pair, 0) + 1

    print("\nMost common misclassifications:")
    for pair, count in sorted(confusion_pairs.items(), key=lambda x: x[1], reverse=True):
        print(f"  {pair}: {count} times")

    # Per-class error analysis
    print("\n" + "="*40)
    print("PER-CLASS ERROR RATES")
    print("="*40)

    for i, class_name in enumerate(class_names):
        class_mask = (y_test == i)
        class_total = np.sum(class_mask)
        class_errors = np.sum((y_test == i) & (y_test_pred != i))
        error_rate = class_errors / class_total * 100 if class_total > 0 else 0

        print(f"{class_name}: {class_errors}/{class_total} errors ({error_rate:.1f}%)")

        # Show what this class gets confused with
        if class_errors > 0:
            confused_with = y_test_pred[(y_test == i) & (y_test_pred != i)]
            unique, counts = np.unique(confused_with, return_counts=True)
            print(f"  Confused with:")
            for pred_class, count in zip(unique, counts):
                print(f"    - {class_names[pred_class]}: {count} times")

else:
    print("No misclassified images - perfect accuracy!")

print("\n✅ Misclassification analysis complete")


