y_val_cat = to_categorical(y_val, num_classes)
y_test_cat = to_categorical(y_test, num_classes)

# ============================================================================
# MOBILENETV2 MODEL DEFINITION - MULTICLASS
# ============================================================================
def create_efficientnet_model(input_shape=(SIZE, SIZE, 3), num_classes=num_classes):
    # Base EfficientNetV2B0
    base_model = EfficientNetV2B0(include_top=False, weights='imagenet', input_shape=input_shape)

    # Freeze all but the last 15 layers
    for layer in base_model.layers[-15:]:
        layer.trainable = True

    # Functional graph
    inputs = Input(shape=input_shape)
    x = base_model(inputs, training=False)
    x = GlobalAveragePooling2D()(x)

    x = Dense(128, activation='relu', kernel_regularizer=l2(0.0001))(x)
    x = BatchNormalization()(x)
    x = Dropout(0.5)(x)

    x = Dense(64, activation='relu', kernel_regularizer=l2(0.0001))(x)
    x = BatchNormalization()(x)
    x = Dropout(0.4)(x)

    x = Dense(32, activation='relu', kernel_regularizer=l2(0.0001))(x)
    x = BatchNormalization()(x)
    x = Dropout(0.3)(x)

    # Output layer - softmax for multiclass
    outputs = Dense(num_classes, activation='softmax', kernel_regularizer=l2(0.0002))(x)

    model = Model(inputs, outputs)

    model.compile(
        optimizer=Adam(learning_rate=4e-5),
        loss='categorical_crossentropy',
        metrics=['accuracy']
    )
    return model

