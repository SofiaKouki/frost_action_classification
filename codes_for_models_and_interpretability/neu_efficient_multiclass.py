    X_fold_aug, y_fold_aug = augment_data(X_fold_train, y_fold_train)
    y_fold_aug_cat = to_categorical(y_fold_aug, num_classes)
    y_fold_val_cat = to_categorical(y_fold_val, num_classes)

    # Fold class weights
