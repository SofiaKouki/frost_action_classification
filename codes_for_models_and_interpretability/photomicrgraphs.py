def predict_standard(model, img_array):
    """Single forward pass for CNN and transfer learning models."""
    img_batch = np.expand_dims(img_array, axis=0)
    prediction = model.predict(img_batch, verbose=0)

    if prediction.shape[-1] == 1:
        frost_prob = float(prediction[0][0])
    else:
        frost_prob = float(prediction[0][1])

    return frost_prob


def predict_bnn(model, img_array, n_samples=20):
    """
    BNN prediction with Monte Carlo Dropout uncertainty.
    Returns: frost_prob_mean, frost_prob_std, all_predictions
    """
    img_batch = np.expand_dims(img_array, axis=0)
    predictions = []

    for _ in range(n_samples):
        pred = model.predict(img_batch, verbose=0)
        if pred.shape[-1] == 1:
            predictions.append(float(pred[0][0]))
        else:
            predictions.append(float(pred[0][1]))

    predictions = np.array(predictions)
    return np.mean(predictions), np.std(predictions), predictions


