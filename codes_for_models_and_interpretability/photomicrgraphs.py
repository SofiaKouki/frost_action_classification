def make_scorecam_heatmap(img_array, model, last_conv_layer_name):
    """
    Score-CAM for CNN and transfer learning models.
    img_array: (1, 512, 512, 3) preprocessed image batch.
    """
    conv_layer_model = tf.keras.models.Model(
        [model.inputs],
        [model.get_layer(last_conv_layer_name).output]
    )
    conv_outputs = conv_layer_model(img_array)[0].numpy()

    input_height, input_width = img_array.shape[1], img_array.shape[2]
    conv_height, conv_width, num_channels = conv_outputs.shape

    scores = []
    for i in range(num_channels):
        activation_map = conv_outputs[:, :, i]

        amin, amax = activation_map.min(), activation_map.max()
        if amax - amin != 0:
            activation_map_norm = (activation_map - amin) / (amax - amin)
        else:
            activation_map_norm = activation_map

        zoom_factor = (input_height / conv_height, input_width / conv_width)
        activation_map_resized = zoom(activation_map_norm, zoom_factor, order=1)

        mask = np.expand_dims(activation_map_resized, axis=-1)
        masked_input = img_array[0] * mask
        masked_input = np.expand_dims(masked_input, axis=0)

        masked_prediction = model.predict(masked_input, verbose=0)
        if masked_prediction.shape[-1] == 1:
            score = masked_prediction[0, 0]
        else:
            score = masked_prediction[0, 1]
        scores.append(score)

    scores = np.array(scores)
    if scores.max() - scores.min() != 0:
        scores = (scores - scores.min()) / (scores.max() - scores.min())

    heatmap = np.zeros((conv_height, conv_width))
    for i in range(num_channels):
        heatmap += scores[i] * conv_outputs[:, :, i]

    heatmap = np.maximum(heatmap, 0)
    if heatmap.max() != 0:
        heatmap = heatmap / heatmap.max()

    return heatmap


def make_scorecam_heatmap_bnn(img_array, model, last_conv_layer_name, mc_samples=5):
    """
    Score-CAM for BNN with MC sampling for consistency.
    Averages predictions across mc_samples forward passes per masked input.
    """
    conv_layer_model = tf.keras.models.Model(
        [model.inputs],
        [model.get_layer(last_conv_layer_name).output]
    )
    conv_outputs = conv_layer_model(img_array)[0].numpy()

    input_height, input_width = img_array.shape[1], img_array.shape[2]
    conv_height, conv_width, num_channels = conv_outputs.shape

    scores = []
    for i in range(num_channels):
        activation_map = conv_outputs[:, :, i]

        amin, amax = activation_map.min(), activation_map.max()
        if amax - amin != 0:
            activation_map_norm = (activation_map - amin) / (amax - amin)
        else:
            activation_map_norm = activation_map

        zoom_factor = (input_height / conv_height, input_width / conv_width)
        activation_map_resized = zoom(activation_map_norm, zoom_factor, order=1)

        mask = np.expand_dims(activation_map_resized, axis=-1)
        masked_input = img_array[0] * mask
        masked_input = np.expand_dims(masked_input, axis=0)

        mc_preds = []
        for _ in range(mc_samples):
            masked_pred = model.predict(masked_input, verbose=0)
            if masked_pred.shape[-1] == 1:
                mc_preds.append(masked_pred[0, 0])
            else:
                mc_preds.append(masked_pred[0, 1])
        score = np.mean(mc_preds)
        scores.append(score)

    scores = np.array(scores)
    if scores.max() - scores.min() != 0:
        scores = (scores - scores.min()) / (scores.max() - scores.min())

    heatmap = np.zeros((conv_height, conv_width))
    for i in range(num_channels):
        heatmap += scores[i] * conv_outputs[:, :, i]

    heatmap = np.maximum(heatmap, 0)
    if heatmap.max() != 0:
        heatmap = heatmap / heatmap.max()

    return heatmap


class ScoreCAM_Transfer:
    """Score-CAM for transfer learning models (MobileNetV2, EfficientNet, ResNet50).
    These have nested base models, so we need to navigate into the sub-model."""
    def __init__(self, model, base_name, layer_candidates):
        self.model = model
        self.layer_name = None
        self.feature_model = None

        base_layer = None
        for layer in model.layers:
            if base_name in layer.name.lower():
                base_layer = layer
                break

        if base_layer is not None:
            for candidate in layer_candidates:
                try:
                    target = base_layer.get_layer(candidate)
                    self.feature_model = tf.keras.models.Model(
                        inputs=base_layer.input, outputs=target.output
                    )
                    self.layer_name = candidate
                    break
                except:
                    continue

            if self.feature_model is None:
                for sublayer in reversed(base_layer.layers):
                    if 'conv' in sublayer.name.lower() or 'project' in sublayer.name.lower():
                        try:
                            if len(sublayer.output_shape) == 4:
                                self.feature_model = tf.keras.models.Model(
                                    inputs=base_layer.input, outputs=sublayer.output
                                )
                                self.layer_name = sublayer.name
                                break
                        except:
                            continue

        if self.feature_model is None:
            raise ValueError(f"Could not find suitable layer for {base_name}")

        feat_shape = self.feature_model.output_shape
        print(f"    Layer: {self.layer_name} | Resolution: {feat_shape[1]}x{feat_shape[2]}")

    def generate_cam(self, input_image):
        if len(input_image.shape) == 3:
            input_image = np.expand_dims(input_image, axis=0)

        feature_maps = self.feature_model.predict(input_image, verbose=0)
        original_score = self.model.predict(input_image, verbose=0)[0]
        if len(original_score.shape) > 0:
            original_score = original_score[0]

        weights = []
        n_features = feature_maps.shape[-1]
        step = max(1, n_features // 50)

        for i in range(0, n_features, step):
            fm = feature_maps[0, :, :, i]
            resized = cv2.resize(fm, (input_image.shape[2], input_image.shape[1]))
            if resized.max() > resized.min():
                normed = (resized - resized.min()) / (resized.max() - resized.min())
            else:
                normed = np.zeros_like(resized)

            masked = input_image[0] * np.expand_dims(normed, axis=-1)
            masked = np.expand_dims(masked, axis=0)
            try:
                score = self.model.predict(masked, verbose=0)[0]
                if len(score.shape) > 0:
                    score = score[0]
                weights.append(score)
            except:
                weights.append(0.0)

        weights = np.array(weights)
        cam = np.zeros((feature_maps.shape[1], feature_maps.shape[2]), dtype=np.float32)
        for idx, i in enumerate(range(0, n_features, step)):
            cam += weights[idx] * feature_maps[0, :, :, i]

        cam = np.maximum(cam, 0)
        if cam.max() > cam.min():
            cam = (cam - cam.min()) / (cam.max() - cam.min())

        cam_resized = cv2.resize(cam, (TARGET_SIZE[0], TARGET_SIZE[1]))
        return cam_resized, original_score


def overlay_heatmap(img_pil, heatmap, alpha=0.4):
    """Overlay Score-CAM heatmap on original full-resolution image."""
    img_array = np.array(img_pil.convert('RGB'))
    heatmap_resized = cv2.resize(heatmap, (img_array.shape[1], img_array.shape[0]))
    heatmap_colored = cv2.applyColorMap(np.uint8(255 * heatmap_resized), cv2.COLORMAP_JET)
    heatmap_colored = cv2.cvtColor(heatmap_colored, cv2.COLOR_BGR2RGB)
    superimposed = heatmap_colored * alpha + img_array
    superimposed = np.clip(superimposed, 0, 255).astype(np.uint8)
    return Image.fromarray(superimposed)


def find_last_conv_layer(model):
    """Find the last convolutional layer in a model."""
    for layer in reversed(model.layers):
        if 'conv' in layer.name.lower() and hasattr(layer, 'output'):
            return layer.name
    return None


