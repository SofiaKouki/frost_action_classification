def preprocess_image(img_pil, target_size=TARGET_SIZE):
    """Resize full photomicrograph to model input size and normalize."""
    if img_pil.mode != 'RGB':
        img_pil = img_pil.convert('RGB')
    img_resized = img_pil.resize(target_size, Image.Resampling.LANCZOS)
    img_array = np.array(img_resized, dtype=np.float32) / 255.0
    return img_array


