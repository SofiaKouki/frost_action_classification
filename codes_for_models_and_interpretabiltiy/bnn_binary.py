"""Additional Score-CAM visualizations for qualitative model interpretation.

This cell:
1. Reuses the previously defined ScoreCAM object, score function, and trained
   BNN to generate supplementary heatmaps.
2. Produces Score-CAM overlays for:
   (a) randomly sampled test images (showing confidence and MC-dropout
       uncertainty alongside class-specific attention),
   (b) selected validation images (both correctly classified and
       misclassified), and
   (c) correctly classified Frost and Non-Frost test samples to compare
       class-specific focus patterns.
3. Saves all panels (random test, validation subset, class-specific examples)
   to disk, enabling detailed visual inspection of what image regions drive
   the model’s decisions across datasets.
"""

# ============================================================================
# ADDITIONAL SCORE-CAM VISUALIZATIONS
# ============================================================================
print("\n" + "="*60)
print("ADDITIONAL SCORE-CAM ANALYSIS")
print("="*60)

if RUN_SCORECAM:
    try:
        # We already have the ScoreCAM object from previous cell
        print("Generating additional Score-CAM visualizations...")

        # Option 1: Show more test samples (random selection)
        print("\n--- Random Test Samples ---")
        np.random.seed(42)  # For reproducible selection
        random_test_indices = np.random.choice(len(X_test), size=6, replace=False)

        X_random = X_test[random_test_indices]
        cam_random = scorecam(score_function, X_random, penultimate_layer='last_conv')

        fig, axes = plt.subplots(2, 3, figsize=(15, 10))
        axes = axes.ravel()

        for i, idx in enumerate(random_test_indices):
            ax = axes[i]

            # Original image
            img = X_test[idx]

            # Overlay CAM
            heatmap = cam_random[i]
            heatmap = np.uint8(255 * heatmap)
            heatmap = cv2.applyColorMap(heatmap, cv2.COLORMAP_JET)
            heatmap = cv2.cvtColor(heatmap, cv2.COLOR_BGR2RGB)
            heatmap = cv2.resize(heatmap, (SIZE, SIZE))

            # Superimpose
            superimposed = cv2.addWeighted(
                (img * 255).astype(np.uint8), 0.6,
                heatmap, 0.4, 0
            )

            ax.imshow(superimposed)
            ax.axis('off')

            # Get prediction for this sample
            pred_prob = mean_test_preds[idx]
            pred_class = y_test_pred[idx]
            true_class = y_test[idx]
            uncertainty = std_test_preds[idx]

            true_label = class_names[true_class]
            pred_label = class_names[pred_class]
            status = "✓" if true_class == pred_class else "✗"

            ax.set_title(
                f"{status} True: {true_label} | Pred: {pred_label}\n"
                f"Conf: {pred_prob:.2f} | Unc: {uncertainty:.2f}",
                fontsize=10
            )

        plt.suptitle("Score-CAM: Random Test Samples", fontsize=16, fontweight='bold')
        plt.tight_layout()
        plt.savefig(os.path.join(save_dir, 'bnn_scorecam_random.png'), dpi=300, bbox_inches='tight')
        plt.show()

        # Option 2: Show validation samples (need to get validation predictions first)
        print("\n--- Validation Samples ---")

        # Get validation predictions
        val_pred_probs = final_model.predict(X_val, batch_size=32, verbose=0)
        val_pred = (val_pred_probs > 0.5).astype(int).flatten()

        # Select interesting validation samples
        val_correct = np.where(val_pred == y_val)[0]
        val_misclassified = np.where(val_pred != y_val)[0]

        val_sample_indices = []
        if len(val_correct) >= 3:
            val_sample_indices.extend(val_correct[:3])
        if len(val_misclassified) >= 3:
            val_sample_indices.extend(val_misclassified[:3])

        if len(val_sample_indices) > 0:
            val_sample_indices = np.array(val_sample_indices[:6])  # Max 6 samples

            X_val_samples = X_val[val_sample_indices]
            cam_val = scorecam(score_function, X_val_samples, penultimate_layer='last_conv')

            n_val_samples = len(val_sample_indices)
            n_rows_val = (n_val_samples + 2) // 3

            fig, axes = plt.subplots(n_rows_val, 3, figsize=(15, 5*n_rows_val))
            if n_rows_val == 1:
                axes = axes if isinstance(axes, np.ndarray) else np.array([axes])
            else:
                axes = axes.ravel()

            for i, idx in enumerate(val_sample_indices):
                ax = axes[i]

                # Original image
                img = X_val[idx]

                # Overlay CAM
                heatmap = cam_val[i]
                heatmap = np.uint8(255 * heatmap)
                heatmap = cv2.applyColorMap(heatmap, cv2.COLORMAP_JET)
                heatmap = cv2.cvtColor(heatmap, cv2.COLOR_BGR2RGB)
                heatmap = cv2.resize(heatmap, (SIZE, SIZE))

                # Superimpose
                superimposed = cv2.addWeighted(
                    (img * 255).astype(np.uint8), 0.6,
                    heatmap, 0.4, 0
                )

                ax.imshow(superimposed)
                ax.axis('off')

                # Validation predictions
                true_label = class_names[y_val[idx]]
                pred_label = class_names[val_pred[idx]]
                pred_prob = val_pred_probs[idx][0]
                status = "✓" if y_val[idx] == val_pred[idx] else "✗"

                ax.set_title(
                    f"{status} True: {true_label} | Pred: {pred_label}\n"
                    f"Confidence: {pred_prob:.2f}",
                    fontsize=10
                )

            # Hide unused subplots
            for i in range(len(val_sample_indices), len(axes)):
                axes[i].axis('off')

            plt.suptitle("Score-CAM: Validation Samples", fontsize=16, fontweight='bold')
            plt.tight_layout()
            plt.savefig(os.path.join(save_dir, 'bnn_scorecam_validation.png'), dpi=300, bbox_inches='tight')
            plt.show()
        else:
            print("No validation samples available for Score-CAM")

        # Option 3: Compare Frost vs Non-Frost focus
        print("\n--- Class-Specific Analysis ---")

        # Get samples by class
        frost_indices = np.where(y_test == 1)[0]
        non_frost_indices = np.where(y_test == 0)[0]

        # Select 3 of each class (correctly classified)
        frost_correct = [idx for idx in frost_indices if y_test_pred[idx] == 1][:3]
        non_frost_correct = [idx for idx in non_frost_indices if y_test_pred[idx] == 0][:3]

        class_indices = frost_correct + non_frost_correct

        if len(class_indices) >= 4:  # At least 2 of each class
            X_class_samples = X_test[class_indices]
            cam_class = scorecam(score_function, X_class_samples, penultimate_layer='last_conv')

            fig, axes = plt.subplots(2, 3, figsize=(15, 10))
            axes = axes.ravel()

            for i, idx in enumerate(class_indices):
                if i >= 6:  # Max 6 samples
                    break

                ax = axes[i]

                # Original image
                img = X_test[idx]

                # Overlay CAM
                heatmap = cam_class[i]
                heatmap = np.uint8(255 * heatmap)
                heatmap = cv2.applyColorMap(heatmap, cv2.COLORMAP_JET)
                heatmap = cv2.cvtColor(heatmap, cv2.COLOR_BGR2RGB)
                heatmap = cv2.resize(heatmap, (SIZE, SIZE))

                # Superimpose
                superimposed = cv2.addWeighted(
                    (img * 255).astype(np.uint8), 0.6,
                    heatmap, 0.4, 0
                )

                ax.imshow(superimposed)
                ax.axis('off')

                true_label = class_names[y_test[idx]]
                pred_prob = mean_test_preds[idx]
                uncertainty = std_test_preds[idx]

                ax.set_title(
                    f"✓ {true_label}\n"
                    f"Conf: {pred_prob:.2f} | Unc: {uncertainty:.2f}",
                    fontsize=11
                )

            # Hide unused subplots
            for i in range(len(class_indices), 6):
                axes[i].axis('off')

            plt.suptitle("Score-CAM: Class-Specific Attention (Top: Frost, Bottom: Non-Frost)",
                        fontsize=16, fontweight='bold')
            plt.tight_layout()
            plt.savefig(os.path.join(save_dir, 'bnn_scorecam_classes.png'), dpi=300, bbox_inches='tight')
            plt.show()

        print("✓ Additional Score-CAM visualizations complete!")

    except Exception as e:
        print(f"⚠ Additional Score-CAM failed: {str(e)}")
        import traceback
        print(traceback.format_exc())

else:
    print("⏭ Skipping additional Score-CAM (RUN_SCORECAM is False)")
