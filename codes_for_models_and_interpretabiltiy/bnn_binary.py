"""End-to-end Bayesian CNN pipeline for frost vs non-frost image classification
with MC Dropout uncertainty and Score-CAM explanations.

Steps:
1. Set seeds and plotting style, mount Google Drive, and define project paths.
2. Load frost/non-frost images from Drive, resize to 512×512, normalize, and
   create stratified train/validation/test splits (saving the test split for reuse).
3. Perform data augmentation on the training set (rotations and flips) and compute
   class weights to handle imbalance.
4. Define a CNN with MC Dropout layers (Bayesian Neural Network-style) and
   a binary cross-entropy loss with label smoothing.
5. Phase 1: Run stratified K-fold cross-validation, plotting training curves and
   confusion matrices for each fold and saving metrics to CSV.
6. Phase 2: Train a final model on the full augmented training data with callbacks
   (ReduceLROnPlateau, ModelCheckpoint, CSVLogger).
7. Evaluate on the held-out test set using MC Dropout to obtain predictive
   mean and uncertainty, generate training curves, confusion matrix, and
   misclassified-image panels.
8. Perform uncertainty analysis (histogram, confidence-vs-uncertainty scatter),
   aggregate final metrics and uncertainty into CSV, and optionally compute
   Score-CAM heatmaps on selected test images for qualitative interpretation.
"""

