class MCDropout(tf.keras.layers.Dropout):
    def call(self, inputs, training=None):
        return super().call(inputs, training=True)


class ScoreCAM_Transfer:
    """Score-CAM for transfer learning models (MobileNetV2, EfficientNet, ResNet50)."""
    def __init__(self, model, base_name, layer_candidates):
        self.model = model
        self.layer_name = None
        self.feature_model = None

        base_layer = None
        for layer in model.layers:
            if base_name in layer.name.lower():
                base_layer = layer
                break

        if base_layer is not None:
            for candidate in layer_candidates:
                try:
                    target = base_layer.get_layer(candidate)
                    self.feature_model = tf.keras.models.Model(
                        inputs=base_layer.input, outputs=target.output
                    )
                    self.layer_name = candidate
                    break
                except:
                    continue

            if self.feature_model is None:
                for sublayer in reversed(base_layer.layers):
                    if 'conv' in sublayer.name.lower() or 'project' in sublayer.name.lower():
                        try:
                            if len(sublayer.output_shape) == 4:
                                self.feature_model = tf.keras.models.Model(
                                    inputs=base_layer.input, outputs=sublayer.output
                                )
                                self.layer_name = sublayer.name
                                break
                        except:
                            continue

        if self.feature_model is None:
            raise ValueError(f"Could not find suitable layer for {base_name}")

        feat_shape = self.feature_model.output_shape
        print(f"  Layer: {self.layer_name} | Resolution: {feat_shape[1]}x{feat_shape[2]}")

    def generate_cam(self, input_image):
        if len(input_image.shape) == 3:
            input_image = np.expand_dims(input_image, axis=0)

        feature_maps = self.feature_model.predict(input_image, verbose=0)
        original_score = self.model.predict(input_image, verbose=0)[0]
        if len(original_score.shape) > 0:
            original_score = original_score[0]

        weights = []
        n_features = feature_maps.shape[-1]
        step = max(1, n_features // 50)

        for i in range(0, n_features, step):
            fm = feature_maps[0, :, :, i]
            resized = cv2.resize(fm, (input_image.shape[2], input_image.shape[1]))
            if resized.max() > resized.min():
                normed = (resized - resized.min()) / (resized.max() - resized.min())
            else:
                normed = np.zeros_like(resized)

            masked = input_image[0] * np.expand_dims(normed, axis=-1)
            masked = np.expand_dims(masked, axis=0)

            try:
                score = self.model.predict(masked, verbose=0)[0]
                if len(score.shape) > 0:
                    score = score[0]
                weights.append(score)
            except:
                weights.append(0.0)

        weights = np.array(weights)
        cam = np.zeros((feature_maps.shape[1], feature_maps.shape[2]), dtype=np.float32)
        for idx, i in enumerate(range(0, n_features, step)):
            cam += weights[idx] * feature_maps[0, :, :, i]

        cam = np.maximum(cam, 0)
        if cam.max() > cam.min():
            cam = (cam - cam.min()) / (cam.max() - cam.min())

        cam_resized = cv2.resize(cam, (SIZE, SIZE))
        return cam_resized, original_score


def scorecam_cnn(model, input_image, conv_layer_name):
    from tf_keras_vis.scorecam import ScoreCAM

    def score_function(output):
        return output[:, 0]

    def model_modifier(m):
        m.layers[-1].activation = tf.keras.activations.linear
        return m

    scorecam = ScoreCAM(model, model_modifier=model_modifier)

    if len(input_image.shape) == 3:
        input_image = np.expand_dims(input_image, axis=0)

    cam = scorecam(score_function, input_image, penultimate_layer=conv_layer_name)
    pred = model.predict(input_image, verbose=0).flatten()[0]
    return cam[0], pred


def scorecam_bnn_mc(model, input_image, conv_layer_name, mc_passes=20):
    def get_conv_and_pred(model, img, layer_name):
        conv_layer = model.get_layer(layer_name)
        intermediate = tf.keras.Model(
            inputs=model.input, outputs=[conv_layer.output, model.output]
        )
        conv_out, pred = intermediate(img, training=False)
        return conv_out, pred

    def single_pass(model, img, layer_name):
        img = tf.cast(img, tf.float32)
        if len(img.shape) == 3:
            img = tf.expand_dims(img, 0)

        conv_out, base_pred = get_conv_and_pred(model, img, layer_name)
        conv_out = conv_out.numpy()[0]
        num_filters = conv_out.shape[-1]
        h, w = conv_out.shape[0], conv_out.shape[1]
        input_h, input_w = img.shape[1], img.shape[2]

        scores = np.zeros(num_filters)
        for f in range(num_filters):
            act = conv_out[:, :, f]
            act = cv2.resize(act, (input_w, input_h))
            amin, amax = act.min(), act.max()
            if amax - amin > 1e-8:
                act = (act - amin) / (amax - amin)
            else:
                act = np.zeros_like(act)

            masked = img.numpy()[0] * act[:, :, np.newaxis]
            masked = tf.expand_dims(masked, 0)
            masked_pred = model(masked, training=False)
            scores[f] = masked_pred.numpy()[0, 0]

        scores_exp = np.exp(scores - scores.max())
        weights = scores_exp / scores_exp.sum()

        heatmap = np.zeros((h, w), dtype=np.float32)
        for f in range(num_filters):
            heatmap += weights[f] * conv_out[:, :, f]

        heatmap = np.maximum(heatmap, 0)
        heatmap = cv2.resize(heatmap, (input_w, input_h))
        hm_max = heatmap.max()
        if hm_max > 1e-8:
            heatmap = heatmap / hm_max
        return heatmap

    mc_heatmaps = []
    mc_preds = []
    for t in range(mc_passes):
        hm = single_pass(model, input_image, layer_name=conv_layer_name)
        mc_heatmaps.append(hm)
        p = model(tf.expand_dims(input_image, 0), training=False).numpy()[0, 0]
        mc_preds.append(p)

    mean_heatmap = np.mean(mc_heatmaps, axis=0)
    mean_pred = np.mean(mc_preds)
    std_pred = np.std(mc_preds)

    return mean_heatmap, mean_pred, std_pred


