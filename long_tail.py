def make_pixel_mask_overlay(image, heatmap, top_pct=20):
    flat = heatmap.flatten()
    threshold_val = np.percentile(flat, 100 - top_pct)
    mask = (heatmap >= threshold_val).astype(np.float32)

    overlay = image.copy()
    for c in range(3):
        overlay[:, :, c] = image[:, :, c] * 0.3 + image[:, :, c] * mask * 0.7

    red_overlay = np.zeros_like(overlay)
    red_overlay[:, :, 0] = mask * 0.3
    overlay = np.clip(overlay + red_overlay, 0, 1)

    if flat.sum() > 0:
        flat_norm = flat / flat.sum()
        mass_covered = flat_norm[flat >= threshold_val].sum() * 100
    else:
        mass_covered = 0

    return overlay, mass_covered


def compute_cumulative_curve(heatmap):
    flat = heatmap.flatten()
    if flat.sum() == 0:
        return None, None
    flat_norm = flat / flat.sum()
    sorted_vals = np.sort(flat_norm)[::-1]
    cumsum = np.cumsum(sorted_vals)
    x_pct = np.linspace(0, 100, len(cumsum))
    return x_pct, cumsum * 100


