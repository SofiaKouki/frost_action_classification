# ============================================================================
# MC DROPOUT LAYER
# ============================================================================
class MCDropout(Dropout):
    def call(self, inputs, training=None):
        return super().call(inputs, training=True)

# ============================================================================
# MULTICLASS BNN MODEL DEFINITION
# ============================================================================
def create_bnn_model(input_shape=(SIZE, SIZE, 3), num_classes=NUM_CLASSES):
    inputs = Input(shape=input_shape)

    # Keep MaxPooling for better feature localization
    x = Conv2D(16, (3, 3), activation='relu')(inputs)
    x = MaxPooling2D((2, 2))(x)
    x = BatchNormalization()(x)

    x = Conv2D(32, (3, 3), activation='relu')(x)
    x = MaxPooling2D((2, 2))(x)
    x = BatchNormalization()(x)

    x = Conv2D(64, (3, 3), activation='relu')(x)
    x = MaxPooling2D((2, 2))(x)
    x = BatchNormalization()(x)

    x = Conv2D(128, (3, 3), activation='relu')(x)
    x = MaxPooling2D((2, 2))(x)
    x = BatchNormalization()(x)

    # Additional conv layer for richer features (better Score-CAM)
    x = Conv2D(190, (3, 3), activation='relu', name='last_conv')(x)
    x = BatchNormalization()(x)

    # Use GlobalMaxPooling for better spatial feature retention
    x = GlobalMaxPooling2D()(x)

    # Larger dense layers for better feature learning
    x = Dense(45, activation='relu', kernel_regularizer=l2(0.0002))(x)
    x = BatchNormalization()(x)
    x = MCDropout(0.5)(x)

    x = Dense(16, activation='relu', kernel_regularizer=l2(0.0002))(x)
    x = BatchNormalization()(x)
    x = MCDropout(0.3)(x)

    outputs = Dense(num_classes, activation='softmax', kernel_regularizer=l2(0.0001))(x)

    model = Model(inputs=inputs, outputs=outputs)
    model.compile(
        optimizer=Adam(learning_rate=0.0002),
        loss='sparse_categorical_crossentropy',
        metrics=['accuracy']
    )
    return model

# ============================================================================
# MC DROPOUT PREDICTION - MULTICLASS
# ============================================================================
def mc_prediction(model, X, T=20):
    # Returns probabilities for all classes
    predictions = np.array([model(X, training=True).numpy() for _ in range(T)])
    mean_pred = predictions.mean(axis=0)  # Shape: (n_samples, num_classes)
    std_pred = predictions.std(axis=0)    # Shape: (n_samples, num_classes)

    # Total uncertainty: average std across all classes
    total_uncertainty = std_pred.mean(axis=1)

    return mean_pred, std_pred, total_uncertainty

