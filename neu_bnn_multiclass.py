"""End-to-end multiclass Bayesian CNN pipeline for microstructure image
classification with MC Dropout uncertainty and Score-CAM explanations.

This cell:
1. Sets seeds, mounts Google Drive, and defines project paths for a multiclass
   microstructure dataset organized in subfolders (one per class).
2. Loads and resizes images from all class directories, normalizes pixel
   values, encodes class labels, and saves a stratified train/validation/test
   split (with the test split persisted for reuse).
3. Applies extensive data augmentation (rotations, flips) and computes class
   weights to mitigate class imbalance in the multiclass setting.
4. Defines a CNN with MC Dropout layers (Bayesian-style) and a softmax
   output, and implements an MC-dropout prediction routine that returns
   per-class predictive means and total uncertainty.
5. Phase 1: Performs stratified K-fold cross-validation, logging per-fold
   metrics (including per-class precision/recall/F1), and saving training
   curves and confusion matrices to disk.
6. Phase 2: Trains a final multiclass model on the full augmented training
   set using callbacks (checkpointing, learning-rate scheduling, CSV logging),
   then evaluates on the held-out test set with MC Dropout to obtain accuracy,
   macro/weighted F1 scores, confusion matrices, and uncertainty-aware plots.
7. Visualizes misclassified test samples together with their predicted class,
   confidence, and uncertainty, summarizes all performance and uncertainty
   statistics to CSV, and optionally generates Score-CAM heatmaps on selected
   test images for qualitative inspection of class-specific attention patterns.
"""

