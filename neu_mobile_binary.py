# Define ScoreCAM class
class ScoreCAM:
    def __init__(self, model, layer_name=None):
        self.model = model
        self.layer_name = layer_name

        # Find the last convolutional layer if layer_name is not specified
        if self.layer_name is None:
            # For MobileNetV2 models, look for the base model layers
            mobilenet_layer = None
            for layer in self.model.layers:
                if 'mobilenet' in layer.name.lower():
                    mobilenet_layer = layer
                    break

            if mobilenet_layer is not None:
                # Look for good layers in MobileNetV2
                for sublayer in reversed(mobilenet_layer.layers):
                    if ('block_16' in sublayer.name and 'project' in sublayer.name) or \
                       ('block_15' in sublayer.name and 'project' in sublayer.name) or \
                       ('block_14' in sublayer.name and 'project' in sublayer.name):
                        self.layer_name = sublayer.name
                        self.base_model = mobilenet_layer
                        break

                # If not found, try any conv layer in the last few blocks
                if self.layer_name is None:
                    for sublayer in reversed(mobilenet_layer.layers):
                        if 'conv' in sublayer.name.lower() or 'project' in sublayer.name.lower():
                            if hasattr(sublayer, 'output_shape') and len(sublayer.output_shape) == 4:
                                self.layer_name = sublayer.name
                                self.base_model = mobilenet_layer
                                break

        if self.layer_name is None:
            # Manual fallback - use a known good layer name
            self.layer_name = 'block_13_project'
            mobilenet_layer = None
            for layer in self.model.layers:
                if 'mobilenet' in layer.name.lower():
                    mobilenet_layer = layer
                    break
            self.base_model = mobilenet_layer

        # Create a model that outputs feature maps from the specified layer
        try:
            if hasattr(self, 'base_model') and self.base_model is not None:
                target_layer = self.base_model.get_layer(self.layer_name)
                self.feature_model = tf.keras.models.Model(
                    inputs=self.base_model.input,
                    outputs=target_layer.output
                )
            else:
                self.feature_model = tf.keras.models.Model(
                    inputs=self.model.input,
                    outputs=self.model.get_layer(self.layer_name).output
                )
        except Exception as e:
            # Try alternative layers
            alternative_layers = ['block_15_project', 'block_14_project', 'block_16_project', 'Conv_1']
            for alt_layer in alternative_layers:
                try:
                    if hasattr(self, 'base_model') and self.base_model is not None:
                        target_layer = self.base_model.get_layer(alt_layer)
                        self.feature_model = tf.keras.models.Model(
                            inputs=self.base_model.input,
                            outputs=target_layer.output
                        )
                        self.layer_name = alt_layer
                        break
                except:
                    continue

        print(f"Score-CAM using layer: {self.layer_name}")

    def generate_cam(self, input_image):
        if len(input_image.shape) == 3:
            input_image = np.expand_dims(input_image, axis=0)

        # Get feature maps
        feature_maps = self.feature_model.predict(input_image, verbose=0)

        # Get original prediction score
        original_score = self.model.predict(input_image, verbose=0)[0]
        if len(original_score.shape) > 0 and original_score.shape[0] > 0:
            original_score = original_score[0]

        # Initialize weights for each feature map
        weights = []

        # Sample every nth feature map to speed up computation
        n_features = feature_maps.shape[-1]
        step = max(1, n_features // 50)  # Use at most 50 feature maps

        for i in range(0, n_features, step):
            # Extract single feature map
            feature_map = feature_maps[0, :, :, i]

            # Resize feature map to input image size
            resized_feature_map = cv2.resize(feature_map, (input_image.shape[2], input_image.shape[1]))

            # Normalize feature map to [0, 1]
            if resized_feature_map.max() > resized_feature_map.min():
                normalized_feature_map = (resized_feature_map - resized_feature_map.min()) / \
                                       (resized_feature_map.max() - resized_feature_map.min())
            else:
                normalized_feature_map = np.zeros_like(resized_feature_map)

            # Create masked image
            masked_image = input_image[0] * np.expand_dims(normalized_feature_map, axis=-1)
            masked_image = np.expand_dims(masked_image, axis=0)

            # Get prediction score for masked image
            try:
                masked_score = self.model.predict(masked_image, verbose=0)[0]
                if len(masked_score.shape) > 0 and masked_score.shape[0] > 0:
                    masked_score = masked_score[0]
                weights.append(masked_score)
            except:
                weights.append(0.0)

        weights = np.array(weights)

        # Generate weighted combination of feature maps
        cam = np.zeros((feature_maps.shape[1], feature_maps.shape[2]), dtype=np.float32)

        for idx, i in enumerate(range(0, n_features, step)):
            cam += weights[idx] * feature_maps[0, :, :, i]

        # Apply ReLU and normalize CAM
        cam = np.maximum(cam, 0)
        if cam.max() > cam.min():
            cam = (cam - cam.min()) / (cam.max() - cam.min())

        return cam, original_score

def overlay_heatmap_scorecam(img, heatmap, alpha=0.4, colormap=cv2.COLORMAP_JET):
    """
    Overlay heatmap on image.

    Args:
        img: Original image (H, W, 3) in range [0, 1]
        heatmap: Heatmap (H, W) in range [0, 1]
        alpha: Transparency of heatmap
        colormap: OpenCV colormap

    Returns:
        Overlayed image
    """
    # Convert heatmap to RGB
    heatmap_uint8 = np.uint8(255 * heatmap)
    heatmap_colored = cv2.applyColorMap(heatmap_uint8, colormap)
    heatmap_colored = cv2.cvtColor(heatmap_colored, cv2.COLOR_BGR2RGB)
    heatmap_colored = heatmap_colored.astype(np.float32) / 255.0

    # Convert image to proper format
    img_display = img.copy()

    # Overlay
    overlayed = heatmap_colored * alpha + img_display * (1 - alpha)
    overlayed = np.clip(overlayed, 0, 1)

    return overlayed

