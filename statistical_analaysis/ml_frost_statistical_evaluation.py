import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import scikit_posthocs as sp
import seaborn as sns
from scipy.stats import friedmanchisquare, studentized_range
import warnings
warnings.filterwarnings('ignore')
from google.colab import drive

# Mount Google Drive
drive.mount('/content/drive')

# =========================================
# LOAD BINARY DATA
# =========================================
file_path = "/content/drive/My Drive/SI_1_ML_Results_Binary_Multiclass.xlsx"
sheet_name_binary = "ml_results_binary"
df_binary = pd.read_excel(file_path, sheet_name=sheet_name_binary)

LAST_COLUMN_BINARY = "Misclassifications Frost"
last_col_idx = df_binary.columns.get_loc(LAST_COLUMN_BINARY)
df_binary = df_binary.iloc[:, :last_col_idx + 1]

# Extract model names for binary
df_binary['Model_Name'] = df_binary['Model'].str.rsplit('_', n=1).str[0]
df_binary['Fold'] = df_binary['Model'].str.rsplit('_', n=1).str[-1]

# Handle independent test set
df_binary_folds = df_binary[df_binary['Fold'].str.match(r'^f\d+$', na=False)]
df_binary_folds['Fold'] = df_binary_folds['Fold'].str.replace('f', '').astype(int)

# Calculate composite score for binary
def calculate_composite_score_binary(row):
    f1_avg = (row['F1 Score Frost'] + row['F1 Score Non frost']) / 2
    accuracy = row['Accuracy']
    inverted_loss = 1 - row['Loss']
    composite = (0.4 * accuracy + 0.4 * f1_avg + 0.2 * inverted_loss)
    return composite

df_binary_folds['Composite_Score'] = df_binary_folds.apply(calculate_composite_score_binary, axis=1)

# Pivot table for binary
pivot_binary = df_binary_folds.pivot_table(
    index='Fold',
    columns='Model_Name',
    values='Composite_Score'
)

# Friedman test for binary
stat_binary, p_value_binary = friedmanchisquare(*[pivot_binary[col].values for col in pivot_binary.columns])
nemenyi_binary = sp.posthoc_nemenyi_friedman(pivot_binary)
avg_ranks_binary = pivot_binary.rank(axis=1, ascending=False).mean().sort_values()

print("="*60)
print("BINARY CLASSIFICATION RESULTS")
print("="*60)
print(f"Friedman statistic: {stat_binary:.4f}, p-value: {p_value_binary:.6f}")
print("\nAverage Ranks:")
print(avg_ranks_binary)

# =========================================
# LOAD MULTICLASS DATA
# =========================================
sheet_name_multiclass = "ml_results_multiclass"
df_multiclass = pd.read_excel(file_path, sheet_name=sheet_name_multiclass)

LAST_COLUMN_MULTICLASS = "Misclassifications Platy-Lenticular"
last_col_idx = df_multiclass.columns.get_loc(LAST_COLUMN_MULTICLASS)
df_multiclass = df_multiclass.iloc[:, :last_col_idx + 1]

# Extract model names for multiclass
df_multiclass['Model_Name'] = df_multiclass['Model'].str.rsplit('_', n=1).str[0]
df_multiclass['Fold'] = df_multiclass['Model'].str.rsplit('_', n=1).str[-1]

# Handle independent test set
df_multiclass_folds = df_multiclass[df_multiclass['Fold'].str.match(r'^f\d+$', na=False)]
df_multiclass_folds['Fold'] = df_multiclass_folds['Fold'].str.replace('f', '').astype(int)

# Calculate composite score for multiclass
def calculate_composite_score_multiclass(row):
    f1_blocky = row['F1-Score Blocky']
    f1_granular = row['F1-Score Granular']
    f1_platy = row['F1-Score Platy-Lenticular']
    macro_f1 = (f1_blocky + f1_granular + f1_platy) / 3
    accuracy = row['Accuracy']
    inverted_loss = 1 - row['Loss']
    composite = (0.4 * accuracy + 0.4 * macro_f1 + 0.2 * inverted_loss)
    return composite

df_multiclass_folds['Composite_Score'] = df_multiclass_folds.apply(calculate_composite_score_multiclass, axis=1)

# Pivot table for multiclass
pivot_multiclass = df_multiclass_folds.pivot_table(
    index='Fold',
    columns='Model_Name',
    values='Composite_Score'
)

# Friedman test for multiclass
stat_multiclass, p_value_multiclass = friedmanchisquare(*[pivot_multiclass[col].values for col in pivot_multiclass.columns])
nemenyi_multiclass = sp.posthoc_nemenyi_friedman(pivot_multiclass)
avg_ranks_multiclass = pivot_multiclass.rank(axis=1, ascending=False).mean().sort_values()

print("\n" + "="*60)
print("MULTICLASS CLASSIFICATION RESULTS")
print("="*60)
print(f"Friedman statistic: {stat_multiclass:.4f}, p-value: {p_value_multiclass:.6f}")
print("\nAverage Ranks:")
print(avg_ranks_multiclass)

# =========================================
# COMBINED CRITICAL DIFFERENCE DIAGRAM
# =========================================
def plot_cd_subplot(ax, avg_ranks, nemenyi_result, pivot_data, label, alpha=0.05):
    """Plot a single critical difference diagram on the given axes"""

    n_models = len(avg_ranks)
    n_folds = len(pivot_data)

    q_alpha = studentized_range.ppf(1 - alpha, n_models, np.inf) / np.sqrt(2)
    cd = q_alpha * np.sqrt(n_models * (n_models + 1) / (6 * n_folds))

    sorted_ranks = avg_ranks.sort_values()
    labels = sorted_ranks.index
    ranks = sorted_ranks.values

    palette = sns.color_palette("colorblind", n_models)
    model_colors = {lbl: palette[i] for i, lbl in enumerate(labels)}

    axis_y = 0.75
    model_label_bottom = 0.15
    rank_min, rank_max = 1, n_models

    # Rank axis
    ax.plot([rank_min, rank_max], [axis_y, axis_y], 'k-', linewidth=2.5)

    # Tick marks + rank numbers
    for i in range(1, n_models + 1):
        ax.plot([i, i], [axis_y - 0.025, axis_y + 0.025], 'k-', linewidth=2)
        ax.text(i, axis_y + 0.04, str(i), ha='center', va='bottom',
                fontsize=16, fontweight='bold')

    # Non-significant pairs (bars under axis)
    non_sig_pairs = []
    for i in range(len(labels)):
        for j in range(i + 1, len(labels)):
            p_val = nemenyi_result.loc[labels[i], labels[j]]
            if p_val > alpha:
                non_sig_pairs.append((ranks[i], ranks[j]))
    non_sig_pairs.sort(key=lambda x: abs(x[1] - x[0]), reverse=True)

    bar_y_positions = {}
    level_height = 0.08
    for rank1, rank2 in non_sig_pairs:
        level = 0
        span = (min(rank1, rank2), max(rank1, rank2))
        for existing_level, existing_spans in bar_y_positions.items():
            conflict = any(not (span[1] < s[0] or span[0] > s[1]) for s in existing_spans)
            if not conflict:
                level = existing_level
                break
        else:
            level = len(bar_y_positions)
        bar_y_positions.setdefault(level, []).append(span)

        bar_y = axis_y - 0.08 - level * level_height
        ax.plot([rank1, rank2], [bar_y, bar_y], 'k-', linewidth=5, solid_capstyle='butt')
        ax.plot([rank1, rank1], [axis_y, bar_y], 'k-', linewidth=2)
        ax.plot([rank2, rank2], [axis_y, bar_y], 'k-', linewidth=2)

    # Model lines downward + labels
    label_positions = {}
    min_distance = 0.5

    for i, (lbl, rank) in enumerate(zip(labels, ranks)):
        y_offset = 0
        for prev_rank, prev_offset in label_positions.items():
            if abs(rank - prev_rank) < min_distance:
                if prev_offset == 0:
                    y_offset = -0.08
                else:
                    y_offset = 0.08

        label_positions[rank] = y_offset
        color = model_colors[lbl]
        line_end_y = model_label_bottom + 0.05 + y_offset

        ax.plot([rank, rank], [axis_y, line_end_y], color=color, linewidth=4)
        ax.scatter(rank, axis_y, s=250, color=color, edgecolors='white', linewidths=2.5, zorder=5)

        label_y = model_label_bottom + y_offset
        ax.text(rank, label_y, f'{lbl}\n({rank:.2f})',
                ha='center', va='top', fontsize=11, fontweight='bold', color=color)

    # Add subplot label (a) or b))
    ax.text(-0.04, 0.98, label, transform=ax.transAxes, fontsize=18,
            fontweight='bold', va='top', ha='left')

    # CD value at bottom-left
    ax.text(rank_min - 0.5, -0.02, f'CD = {cd:.3f}',
            fontsize=13, fontweight='bold',
            bbox=dict(boxstyle='round,pad=0.5', facecolor='wheat', edgecolor='black'))

    ax.set_xlim(rank_min - 0.5, rank_max + 0.5)
    ax.set_ylim(-0.05, 0.95)
    ax.set_xticks(range(rank_min, rank_max + 1))
    ax.set_yticks([])
    for spine in ['right', 'left', 'top']:
        ax.spines[spine].set_visible(False)

    return cd

# Create combined figure
sns.set_style("darkgrid")
